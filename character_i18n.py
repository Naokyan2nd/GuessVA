def split_display_name(name: str) -> tuple[str, str]:
    match = re.search(r'（([^）]+)）\s*$', name)
    if not match:
        return name, name
    zh = name[:match.start()].strip() or match.group(1).strip()
    ja = match.group(1).split('·')[0].split('/')[0].strip()
    return zh, ja


def _translate_title(value: str) -> str:
    if value in TITLE_JA:
        return TITLE_JA[value]
    translated = value
    for source, target in TITLE_REPLACEMENTS:
        translated = translated.replace(source, target)
    return translated


def _build_work_lookup(characters: list[dict], base_dir: Path) -> dict[tuple[str, str], str]:
    try:
        from enrich_from_wiki import (
            WORK_CN_FALLBACK,
            build_work_cn_map,
            parse_wiki_roles,
            work_cn,
        )

        wiki_text = (base_dir / '_wiki.txt').read_text(encoding='utf-8')
        roles = parse_wiki_roles(wiki_text)
        mapping = build_work_cn_map(characters, roles)
        mapping.update(WORK_CN_FALLBACK)
        lookup: dict[tuple[str, str], str] = {}
        for role in roles:
            canonical_work = work_cn(role['work_jp'], mapping)
            lookup.setdefault((role['char_jp'], canonical_work), role['work_jp'])
        for japanese, canonical in mapping.items():
            lookup.setdefault(('', canonical), japanese)
        return lookup
    except (ImportError, OSError, ValueError):
        return {}


def prepare_character_localizations(characters: list[dict], base_dir: Path) -> None:
    work_lookup = _build_work_lookup(characters, base_dir)
    for char in characters:
        zh_name, ja_name = split_display_name(char.get('名前', ''))
        canonical_work = char.get('初登場の作品', '')
        ja_work = work_lookup.get((ja_name, canonical_work))
        if not ja_work:
            ja_work = work_lookup.get(('', canonical_work), canonical_work)
        char['_i18n'] = {
            'zh': {'名前': zh_name, '初登場の作品': canonical_work},
            'ja': {'名前': ja_name, '初登場の作品': ja_work},
        }


def display_value(char: dict, field: str, value, language: str):
    if field in {'名前', '初登場の作品'}:
        return char.get('_i18n', {}).get(language, {}).get(field, value)
    if language != 'ja' or not isinstance(value, str):
        return value
    if field == '肩書':
        return _translate_title(value)
    return ATTRIBUTE_JA.get(value, value)


def localized_character(char: dict, language: str) -> dict:
    result = dict(char)
    display = {}
    for field in DISPLAY_FIELDS:
        value = char.get(field)
        if isinstance(value, list):
            display[field] = [display_value(char, field, item, language) for item in value]
        else:
            display[field] = display_value(char, field, value, language)
    result['_display'] = display
    result.pop('_i18n', None)
    return result
