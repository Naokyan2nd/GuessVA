def get_language(req=None) -> str:
    req = req or request
    language = req.cookies.get('language', 'ja')
    return language if language in SUPPORTED_LANGUAGES else 'ja'


def translate(text: str, **values) -> str:
    translated = ZH_TRANSLATIONS.get(text, text) if get_language() == 'zh' else text
    return translated.format(**values) if values else translated


def javascript_translations() -> dict:
    return ZH_TRANSLATIONS if get_language() == 'zh' else {}
